public class casting {
    public static void main(String[] args) {
        //type casting is here!! and it happend automatically it is called implict type casting
        int a=3;
        double b=a;
        System.out.println(b);

        //explicit type casting which externally done by the developer
        double num1=45.5;
        int num2;
        num2=(int)num1;
        System.out.println(num2);

         int x=12;
         int y=5;
         float z=x/y;
        System.out.println(z);


    }

}
